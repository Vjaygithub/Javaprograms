//Example for Arithmetic Operators
import java.io.*;
class AriOpe
{
	public static void main(String args[])
	{
		System.out.println("Arithmetic Operators");
		System.out.println("Addition 100+3 " +(100+3));
		System.out.println("Substraction 200-150 " +(200-150));
		System.out.println("Mutliplication 80*50 " +(80*50));
		System.out.println("Division 100/2 " +(100/2));
		System.out.println("Modulus 100%2 " +(100%2));
	}
}
