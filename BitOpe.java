//Example for Bitwise Operator
import java.io.*;
class BitOpe
{
	public static void main(String args[])
	{
		System.out.println("Bitwise Operators");
		System.out.println("15&8 =" + (15&8));
		System.out.println("15|8 =" +(15|8));
		System.out.println("~-10 =" +(~-10));
		int x=10;
		int y=x<<3;
		System.out.println("Reft shift operator <<3=" +y);
		x=10;
		y=x>>2;
		System.out.println("Right shift operator >>2=" +y);
	}
}
		