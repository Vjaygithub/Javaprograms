//Example for Increment Operatot
import java.io.*;
class IncPopre
{
	public static void main(String args[])
	{
		int a=10;
		System.out.println("a =" +a);
		System.out.println("Post increment");
		System.out.println("a++ =" +a++);
		System.out.println("a ="+a);
		System.out.println("Pre increment");
		System.out.println("++a ="+(++a));
		System.out.println("a =" +a);
	}
}
		