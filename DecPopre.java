//Example for Decrement Operator
import java.io.*;
class DecPopre
{
	public static void main(String args[])
	{
		int a=50;
		System.out.println("a= "+a);
		System.out.println("a-- =" +a--);
		System.out.println("Post decrement");
		System.out.println("a =" +a);
		System.out.println("Pre decrement");
		System.out.println("--a =" +(--a));
	}
}
		